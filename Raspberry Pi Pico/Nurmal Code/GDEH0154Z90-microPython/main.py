def EPD_Update(): 
    Epaper_Write_Command(0x22) #Display Update Control
    Epaper_Write_Data(0xF7)   
    Epaper_Write_Command(0x20) #Activate Display Update Sequence
    Epaper_READBUSY()   

#display 
def EPD_WhiteScreen_ALL(data_BW,data_R):
    Epaper_Write_Command(0x24)   #write RAM for black(0)/white (1)
    for i in range(5000):             
        Epaper_Write_Data(data_BW[i])
    Epaper_Write_Command(0x26)   #write RAM for black(0)/white (1)
    for i in range(5000):             
        Epaper_Write_Data(0xff-data_R[i])
    EPD_Update()	 


#Clear screen
def PIC_display_Clear():
    #Write Data
    Epaper_Write_Command(0x24)	       #Transfer old data
    for i in range(5000):  	
        Epaper_Write_Data(0xFF)
    Epaper_Write_Command(0x26)	       #Transfer old data
    for i in range(5000):  	
        Epaper_Write_Data(0x00) 
    #Refresh
    EPD_Update()
#Enter deep sleep mode 
def EPD_DeepSleep(): 	
    Epaper_Write_Command(0x10) #enter deep sleep
    Epaper_Write_Data(0x01) 
    utime.sleep(0.01)


 
